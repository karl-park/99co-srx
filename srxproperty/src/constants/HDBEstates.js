export const getHDBEstateDescription = hdbKey => {
  for (let entry of Object.entries(HDBTown)) {
    const key = entry[0];
    const value = entry[1];
    if (value == hdbKey) {
      return HDBTownDescription[key];
    }
  }

  return "";
};
